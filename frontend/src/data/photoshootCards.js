// Helper function to get card by ID
export const getCardById = (cardId) => {
  return photoshootCards.find(card => card.id === cardId);
};

// Helper function to get webhook key by card ID
export const getWebhookKeyByCardId = (cardId) => {
  const card = getCardById(cardId);
  return card ? card.webhookKey : null;
};

// Helper function to get cards by theme
export const getCardsByTheme = (theme) => {
  return photoshootCards.filter(card => card.theme === theme);
};

